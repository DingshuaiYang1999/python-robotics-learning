# Python 基础 - 机器人编程入门

# ===== 数字 =====
robot_speed = 1.5        # 机器人速度 (m/s) - 小数
wheel_count = 4          # 轮子数量 - 整数
is_moving = True         # 是否在移动 - 布尔值

# ===== 文字 =====
robot_name = "RoboBot"   # 机器人名字 - 字符串

# ===== 打印输出 =====
print("机器人名字:", robot_name)
print("速度:", robot_speed, "m/s")
print("轮子数量:", wheel_count)
print("正在移动:", is_moving)

# ===== 数学运算 =====
max_speed = 3.0
current_speed = 1.5
speed_ratio = current_speed / max_speed

print("\n速度比例:", speed_ratio)
print("速度百分比:", speed_ratio * 100, "%")